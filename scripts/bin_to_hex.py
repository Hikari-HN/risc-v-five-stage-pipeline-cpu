def is_number(s):
    try:
        float(s)
        return True
    except ValueError:
        return False


def bin_to_hex(bin_val, hex_width):
    if (not is_number(bin_val)):
        raise ValueError('bin_to_hex: parameter {} is not a number'.format(
            bin_val))

    return format(int(bin_val, 2), 'X').zfill(hex_width)


